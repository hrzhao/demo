    private void btn_sendMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_sendMouseClicked
        // TODO add your handling code here:
        Long s = new Date().getTime();
        String srcXml = getXml();
        send(srcXml);
        Long e = new Date().getTime();
        String txtStr = txt_result.getText();
        txtStr += "{" + (e - s)+"}";
        txt_result.setText(txtStr);
    }//GEN-LAST:event_btn_sendMouseClicked

    private int count = 0;
    private void send(String srcXml){
        count++;
         try {
            String urlStr = txt_url.getText();
            URL url = new URL(urlStr);
            URLConnection con = url.openConnection();
            con.setDoOutput(true);
            con.setRequestProperty("Pragma:", "no-cache");
            con.setRequestProperty("Cache-Control", "no-cache");
            con.setRequestProperty("Content-Type", "text/xml");
             try (OutputStreamWriter out = new OutputStreamWriter(con.getOutputStream())) {
                 String outText= txt_result.getText();
                 outText += "\n|-------------------------------"+count+"------------------------------------|\n";
                 outText += urlStr + "\n";
                 outText += srcXml + "\n";
                 txt_result.setText(outText);
                 out.write(new String(srcXml.getBytes("UTF-8")));
                 out.flush();
             }
            BufferedReader br = new BufferedReader(new InputStreamReader(con.getInputStream(),"UTF-8"));
            String line = "";
            for (line = br.readLine(); line != null; line = br.readLine()) {
                 String outText= txt_result.getText();
                outText += line + "\n";
                txt_result.setText(outText);
            }
        } catch (MalformedURLException e) {
            String outText= txt_result.getText();
            outText += e.toString() + "\n";
            txt_result.setText(outText);
        } catch (IOException e) {
            String outText= txt_result.getText();
            outText += e.toString() + "\n";
            txt_result.setText(outText);
        }
    }
    private String getXml(){
        StringBuilder sb = new StringBuilder();
        String content = txt_content.getText();
        String fromUserName = txt_fromUserName.getText();
        String toUserName = txt_toUserName.getText();
        String msgId = txt_msgId.getText();
        String  msgType = txt_msgType.getText();
        Date date = new Date();
        Long createTime = date.getTime()/100;
        if(msgId == null || msgId.equals("")){
            msgId = date.getTime()+"";
        }
        sb.append("<xml>\n");
        sb.append(("\t<ToUserName><![CDATA["+toUserName+"]]></ToUserName>\n"));
        sb.append(("\t<FromUserName><![CDATA["+fromUserName+"]]></FromUserName>\n"));
        sb.append(("\t<CreateTime>" + createTime + "</CreateTime>\n"));
        sb.append(("\t<MsgType><![CDATA["+msgType+"]]></MsgType>\n"));
        sb.append(("\t<Content><![CDATA[" + content + "]]></Content>\n"));
        sb.append(("\t<MsgId>"+msgId+"</MsgId>\n"));
        sb.append(("</xml>"));
        return sb.toString();
    }
